// Risk-contribution panel: capital% vs RISK% (volatility / beta / tail) per asset class, from the
// precomputed risk_contribution_snapshot (Euler decomposition, Ledoit-Wolf shrinkage covariance).
// The headline is the GAP — a class small in capital but large in risk (concentration), or a
// negative Tail% (a crash hedge that REDUCES drawdown). Non-marked holdings (privates/CDs/annuities)
// are excluded + surfaced as a coverage caveat. 3Y default with a 1Y toggle.
function renderRisk() {
  if (!el.risk) return;
  const S = RISK_SNAPSHOT;
  if (!S || !S.data || !S.slices) { el.risk.innerHTML = ""; el.risk.style.display = "none"; return; }
  el.risk.style.display = "";
  const slices = S.slices;
  const sliceKey = (state.riskSlice && S.data[state.riskSlice]) ? state.riskSlice : "Total";
  const sliceMeta = slices.find((s) => s.key === sliceKey) || {};
  const scope = S.data[sliceKey] || S.data["Total"] || {};
  // Scope × Within: an optional within-asset-class breakdown of the chosen scope (e.g. the
  // Living Trust's equity sub-book). Reset/ignore the within if it isn't available in this scope.
  const withinAvail = sliceMeta.within || [];
  let withinKey = state.riskWithin;
  if (withinKey && !withinAvail.includes(withinKey)) withinKey = null;
  const source = (withinKey && scope.within && scope.within[withinKey]) ? scope.within[withinKey] : scope;
  const sliceLabel = (sliceMeta.label || sliceKey) + (withinKey ? ` · Within ${withinKey}` : "");
  const sliceWindows = (source.windows) || {};
  const winKey = (state.riskWindow && sliceWindows[state.riskWindow]) ? state.riskWindow : "3Y";
  const W = sliceWindows[winKey] || sliceWindows["3Y"] || Object.values(sliceWindows)[0];
  // scope <select> (Total → tax tracks → accounts) + within <select> + window toggle
  const groupLabel = { tax: "Tax track", account: "Account" };
  const scopeOpts = ["all", "tax", "account"].map((g) => {
    const items = slices.filter((s) => s.group === g);
    if (!items.length) return "";
    const opts = items.map((s) =>
      `<option value="${escapeAttr(s.key)}" ${s.key === sliceKey ? "selected" : ""}>${escapeHtml(s.label)}${s.mv ? ` — $${(s.mv / 1e6).toFixed(1)}M` : ""}</option>`).join("");
    return g === "all" ? opts : `<optgroup label="${groupLabel[g]}">${opts}</optgroup>`;
  }).join("");
  const withinOpts = `<option value="">(whole slice — by asset class)</option>` +
    withinAvail.map((b) => `<option value="${escapeAttr(b)}" ${b === withinKey ? "selected" : ""}>Within ${escapeHtml(b)}</option>`).join("");
  const withinSel = withinAvail.length
    ? `<label class="rkSliceWrap">Within <select class="rkWithin">${withinOpts}</select></label>` : "";
  const wbtns = ["3Y", "1Y"].filter((k) => sliceWindows[k]).map((k) =>
    `<button class="rkWin ${k === winKey ? "active" : ""}" data-win="${k}">${k}</button>`).join("");
  const controls = `<div class="rkControls"><label class="rkSliceWrap">View <select class="rkSlice">${scopeOpts}</select></label>${withinSel}<span class="rkWindows">Lookback ${wbtns}</span></div>`;
  const header = acctCaveatBadge() + `<div class="panelHeader"><div><p class="eyebrow">Where the risk actually is — capital vs risk, by asset class · <strong>standalone per slice</strong></p><h2>Risk Contribution</h2></div><span class="pill" title="${escapeAttr(S.method || "")}">${escapeHtml(sliceLabel)}</span></div>`;
  const wire = () => {
    const sel = el.risk.querySelector(".rkSlice");
    if (sel) sel.addEventListener("change", () => { state.riskSlice = sel.value; state.riskWithin = null; renderRisk(); });
    const wsel = el.risk.querySelector(".rkWithin");
    if (wsel) wsel.addEventListener("change", () => { state.riskWithin = wsel.value || null; renderRisk(); });
    el.risk.querySelectorAll(".rkWin").forEach((b) => b.addEventListener("click", () => { state.riskWindow = b.getAttribute("data-win"); renderRisk(); }));
  };
  if (!W || W.error) {
    el.risk.innerHTML = header + controls +
      `<p class="planNote">Not available for <strong>${escapeHtml(sliceLabel)}</strong> (${winKey}): ${escapeHtml((W && W.error) || "no data")}${W && W.marked != null ? ` — only ${W.marked} marked holding(s) with history` : ""}. Pick another slice, or re-run <code>portfolio_analysis.py</code>.</p>`;
    wire();
    return;
  }
  const pf = W.portfolio;
  const rows = W.by_asset_class.map((r) => {
    const hedge = r.es_pct < 0;
    const conc = r.vol_pct - r.capital_pct_marked;        // risk-vs-capital gap
    const cls = hedge ? "rkHedge" : (conc > 8 ? "rkConc" : "");
    const barW = Math.max(0, Math.min(100, r.vol_pct));
    return `<tr class="${cls}"><td class="rkName">${escapeHtml(r.key)}</td><td class="rkNum">${r.capital_pct_marked.toFixed(1)}</td><td class="rkBarCell"><div class="rkBar"><span style="width:${barW}%"></span></div><em>${r.vol_pct.toFixed(1)}</em></td><td class="rkNum">${r.beta_pct.toFixed(1)}</td><td class="rkNum ${hedge ? "rkNeg" : ""}">${r.es_pct.toFixed(1)}</td></tr>`;
  }).join("");
  el.risk.innerHTML = header + controls + `
    <div class="rkCaveat">⚠️ <strong>${(W.non_marked_weight * 100).toFixed(0)}%</strong> of <strong>${escapeHtml(sliceLabel)}</strong> ($${Math.round(W.non_marked_mv).toLocaleString()}) is <strong>non-marked</strong> (CDs / annuities / private / illiquid — no honest market series) and <strong>excluded</strong>. The risk %s below are of the measurable sub-book and sum to 100%.</div>
    <div class="planMetrics metrics">
      <div class="metric"><span>Volatility (annualized)</span><strong>${(pf.vol_annual * 100).toFixed(1)}%</strong></div>
      <div class="metric"><span>Portfolio beta (vs SPY)</span><strong>${pf.beta.toFixed(2)}</strong></div>
      <div class="metric"><span>Expected shortfall (worst ${(pf.es_alpha * 100).toFixed(0)}% days)</span><strong>${(pf.es_daily * 100).toFixed(2)}%/day</strong></div>
    </div>
    <table class="rkTable">
      <thead><tr>
        <th>Asset class</th>
        <th title="% of the marked (measurable) sub-book by capital">Capital&nbsp;%</th>
        <th title="component contribution to THIS slice's volatility — Ledoit-Wolf shrinkage covariance">Vol&nbsp;%</th>
        <th title="component contribution to THIS slice's beta">Beta&nbsp;%</th>
        <th title="component contribution to tail / expected-shortfall — NEGATIVE = helps in a crash">Tail&nbsp;%</th>
      </tr></thead>
      <tbody>${rows}</tbody>
    </table>
    <div class="planNote">Each slice is decomposed <strong>standalone</strong> — its own &sigma;/&beta;/ES, weights renormalized inside the slice. Columns are exact <strong>Euler decompositions</strong>. <strong>Vol&nbsp;% &gt; Capital&nbsp;%</strong> = carries more risk than money (shaded amber); <strong class="good">negative Tail&nbsp;%</strong> = a crash hedge that <em>reduces</em> drawdown (shaded green). ${winKey} daily · ${W.obs}d (${escapeHtml(W.start)}&rarr;${escapeHtml(W.end)}) · snapshot ${escapeHtml((S.generated || "").slice(0, 10))}.</div>`;
  wire();
}

// Convexity panel: (1) live composition by the 7 convex roles, Convexity foregrounded;
// (2) gap to an editable target + the deploy-$ that closes it (from the overlay snapshot);
// (3) precomputed per-role crisis attribution (snapshot.crisis). Convexity here =
// trend / managed-futures / long-short (the LIQALTS + trend roles).
const _CONVEX_ROLE_ORDER = ["Growth","Income","Duration","Convexity","Diversifier","Other-Alt","Cash","Other"];

function renderConvexity(cube) {
  if (!el.convexity) return;
  const total = cube.totalValue || 0;
  const roleTot = {};
  for (const s of cube.sleeves) {
    const r = convexRoleForSleeve(s.sleeve);
    roleTot[r] = (roleTot[r] || 0) + s.value;
  }
  const convexVal = roleTot["Convexity"] || 0;
  const convexPct = total ? convexVal / total : 0;
  // The Convexity role lumps genuine crisis-alpha trend/managed-futures together with
  // long-short equity (AQR Flex / Delphi / CLSE). For the thesis they're NOT the same —
  // only trend/MF is the convex crash hedge — so split them out. The gap + deploy target
  // DEDICATED TREND, the actual lever (and the ~0% Marc flagged).
  const _TREND_SLEEVES = new Set(["Managed Futures", "Trend Following", "Trend Following Managed Futures"]);
  let trendVal = 0;
  for (const s of cube.sleeves) if (_TREND_SLEEVES.has(s.sleeve)) trendVal += s.value;
  const trendPct = total ? trendVal / total : 0;
  const lsVal = Math.max(0, convexVal - trendVal);            // long-short portion of the role
  const target = state.planning.convexTrendTargetPct ?? 0.05; // target DEDICATED TREND %
  const gapPct = target - trendPct;
  const gapUsd = gapPct * total;

  const maxRole = Math.max(1, ..._CONVEX_ROLE_ORDER.map((r) => roleTot[r] || 0));
  const compRows = _CONVEX_ROLE_ORDER.filter((r) => (roleTot[r] || 0) > 0).map((r) => {
    const v = roleTot[r] || 0;
    const hot = r === "Convexity";
    return `<div class="cxRow ${hot ? "cxHot" : ""}"><span class="cxLabel">${r}${hot ? " ◆" : ""}</span>` +
           `<span class="cxBar"><i style="width:${(100 * v / maxRole).toFixed(1)}%"></i></span>` +
           `<strong>${money(v)}</strong><em>${percent(total ? v / total : 0)}</em></div>`;
  }).join("");

  const S = OVERLAY_SNAPSHOT;
  const deployRows = (S && S.deploy ? S.deploy : []).map((d) =>
    `<div class="cxRow"><span class="cxLabel">Deploy $${d.usd_m.toFixed(1)}M (${percent(d.pct)}) → trend</span>` +
    `<em>CAGR/DV ${cagrDownVol(S.current).toFixed(2)} → ${cagrDownVol(d).toFixed(2)}</em>` +
    `<strong class="${d.ret_2022 >= 0 ? "good" : "warn"}">2022 ${signPct(d.ret_2022)}</strong></div>`).join("");

  let crisisBlock = "";
  if (S && S.crisis && Object.keys(S.crisis).length) {
    const wins = Object.keys(S.crisis);
    const roles = ["Growth", "Income", "Duration", "Convexity", "Cash"];
    const head = `<tr><th>Role</th>${wins.map((w) => `<th>${escapeHtml(w)}</th>`).join("")}</tr>`;
    const body = roles.map((r) => {
      const cells = wins.map((w) => {
        const v = S.crisis[w][r];
        return v == null ? "<td>—</td>" : `<td class="${v >= 0 ? "good" : "warn"}">${signPct(v)}</td>`;
      }).join("");
      return `<tr class="${r === "Convexity" ? "cxHot" : ""}"><td>${r}${r === "Convexity" ? " ◆" : ""}</td>${cells}</tr>`;
    }).join("");
    crisisBlock = `<div class="cxBlock"><p class="eyebrow">Per-role crisis attribution (precomputed)</p>` +
      `<div class="cxScroll"><table class="cxCrisis"><thead>${head}</thead><tbody>${body}</tbody></table></div>` +
      `<div class="planNote">${escapeHtml(S.note || "")} 2008 GFC predates the trend-data window.</div></div>`;
  }

  el.convexity.innerHTML = `
    <div class="panelHeader">
      <div><p class="eyebrow">Tactical convexity &middot; held instruments</p><h2>Crash-Shape &amp; Convexity</h2></div>
      <span class="pill ovPill">Convexity = trend / managed-futures / long-short</span>
    </div>
    <div class="planNote"><strong>Convexity has two sources.</strong> This panel is <em>tactical</em> convexity — the crash-hedge instruments you hold. <em>Structural</em> convexity comes from how the book is sized &amp; rebalanced (volatility targeting) — see the <strong>Sortino Overlay</strong> panel below. A holdings view sees only the tactical half; a vol-managed book can be far more convex than its instruments suggest.</div>
    <div class="planMetrics metrics">
      <div class="metric"><span>Convexity sleeve (broad)</span><strong>${percent(convexPct)} · ${money(convexVal)}</strong></div>
      <div class="metric ${trendPct >= target ? "good" : "warn"}"><span>Dedicated trend / managed-futures</span><strong>${percent(trendPct)} · ${money(trendVal)}</strong></div>
      <label class="metric cxTarget"><span>Target trend %</span><input type="number" step="0.5" data-cx="trend" value="${(target * 100).toFixed(1)}" /></label>
      <div class="metric ${gapPct <= 0 ? "good" : "warn"}"><span>${gapPct > 0 ? "Gap → deploy to trend" : "At/above target"}</span><strong>${gapPct > 0 ? money(gapUsd) : "✓"}</strong></div>
    </div>
    <div class="planNote">The Convexity role's ${money(convexVal)} is mostly <strong>long-short equity</strong> (${money(lsVal)}, AQR Flex / Delphi) — only ${money(trendVal)} is genuine <strong>trend / managed-futures</strong>, the actual crash hedge. The gap + deploy target that.</div>
    <div class="cxBlock"><p class="eyebrow">Composition by convex role</p>${compRows}</div>
    ${deployRows ? `<div class="cxBlock"><p class="eyebrow">Close the gap — idle cash → standalone trend (~$0 tax)</p>${deployRows}</div>` : ""}
    ${crisisBlock}`;

  el.convexity.querySelectorAll("input[data-cx]").forEach((inp) => {
    inp.addEventListener("change", () => {
      const v = Number(inp.value);
      if (!Number.isFinite(v) || v < 0) return;
      state.planning = { ...state.planning, convexTrendTargetPct: v / 100 };
      saveJson("planning", state.planning);
      render();
    });
  });
}

