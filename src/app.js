function styleSizeOfSleeve(sleeve) {
  const t = (sleeve || "").toLowerCase();
  // 2026-06-26: gate on equity-ness so EQUITY that lacks a US Morningstar style-box no longer
  // dumps into "Non-equity" (the old behavior mislabeled private equity + EM + broad International
  // as non-equity). Order: explicit private equity → size×style grid → sector → other equity
  // buckets → genuine non-equity.
  if (t === "buyout") return "Private Equity";                          // equity, but no public style-box
  const size = /\blarge\b/.test(t) ? "Large" : /\bmid/.test(t) ? "Mid" : /\bsmall\b/.test(t) ? "Small" : null;
  const style = /\bvalue\b/.test(t) ? "Value" : /\bgrowth\b/.test(t) ? "Growth" : /\bblend\b/.test(t) ? "Blend" : null;
  if (size && style) return `${size} ${style}`;
  if (size) return `${size} Blend`;
  if (/technology|industrials?|utilities|energy|real estate|natural resources|materials|health/.test(t)) return "Sector / Thematic";
  if (/emerging market/.test(t)) return "Emerging Markets";            // equity, no US style-box
  if (/\binternational\b|foreign/.test(t)) return "International";      // equity, no US style-box
  // residual equity safety net — gate on the equity SET, NOT the `style` word (so "stable VALUE",
  // "annuity" etc. don't get mislabeled equity by an incidental value/growth/blend substring).
  if (_AC_EQUITY.has(sleeve)) return "Equity — other";
  return "Non-equity";                                                 // genuinely not equity (bond / cash / alt / trend)
}
