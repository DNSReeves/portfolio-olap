function parseCsv(text) {
  const rows = parseCsvRows(text);
  const [headerRow, ...dataRows] = rows.filter((row) => row.some((cell) => cell.trim()));
  if (!headerRow) return [];
  const headers = headerRow.map((header, index) => header.trim() || `Column ${index + 1}`);
  return dataRows.map((row) => Object.fromEntries(headers.map((header, index) => [header, (row[index] || "").trim()])));
}

function parseCsvRows(input) {
  const rows = [];
  let row = [];
  let cell = "";
  let inQuotes = false;

  for (let index = 0; index < input.length; index += 1) {
    const char = input[index];
    const next = input[index + 1];
    if (char === '"' && inQuotes && next === '"') {
      cell += '"';
      index += 1;
    } else if (char === '"') {
      inQuotes = !inQuotes;
    } else if (char === "," && !inQuotes) {
      row.push(cell);
      cell = "";
    } else if ((char === "\n" || char === "\r") && !inQuotes) {
      if (char === "\r" && next === "\n") index += 1;
      row.push(cell);
      rows.push(row);
      row = [];
      cell = "";
    } else {
      cell += char;
    }
  }
  row.push(cell);
  rows.push(row);
  return rows;
}

// P3-36 (2026-07-03): two-pass, priority-ordered, claim-once column detection.
// The old single-pass "first header that exact-OR-substring matches ANY alias" let
// a bare alias win over a later exact one and let one header serve two fields:
// "Account Name" was claimed as assetName ("account name".includes("name")) so
// name-keyword classification ran against the account nickname; a Schwab
// "% of Acct (% of Account)" column was claimed as `account`. Fix:
//   • exact normalized equality across ALL headers first, substring only as a
//     fallback second (so an exact "Security Name" beats a substring "Account Name");
//   • iterate each field's aliases in PRIORITY order (find the header per alias);
//   • never let a header already claimed by an earlier field be reclaimed;
//   • ignore "%" columns entirely — a percentage is never a mapped dimension.
function detectColumnMapping(headers) {
  const mapping = {};
  const claimed = new Set();
  const cand = headers.filter((h) => !h.includes("%")).map((h) => ({ raw: h, norm: normalizeHeader(h) }));
  const claim = (field, pred) => {
    if (mapping[field]) return;
    for (const alias of COLUMN_ALIASES[field]) {
      const na = normalizeHeader(alias);
      const hit = cand.find((h) => !claimed.has(h.raw) && pred(h.norm, na));
      if (hit) { mapping[field] = hit.raw; claimed.add(hit.raw); return; }
    }
  };
  const fields = Object.keys(COLUMN_ALIASES);
  fields.forEach((field) => claim(field, (hn, an) => hn === an));       // pass 1: exact
  fields.forEach((field) => claim(field, (hn, an) => hn.includes(an))); // pass 2: substring fallback
  return mapping;
}

function normalizeHoldings(rows, mapping, fallbackValuationDate) {
  const holdings = [];
  const errors = [];
  rows.forEach((row, index) => {
    const ticker = normalizeTicker(read(row, mapping.ticker));
    const assetName = read(row, mapping.assetName) || ticker || `Row ${index + 1}`;
    const valuationDate = normalizeDate(read(row, mapping.valuationDate)) || fallbackValuationDate;
    const shares = parseNumber(read(row, mapping.shares));
    const price = parseNumber(read(row, mapping.price));
    const importedMarketValue = parseNumber(read(row, mapping.marketValue));
    const marketValue = importedMarketValue || shares * price;
    if (!valuationDate) {
      errors.push(`Row ${index + 1}: missing valuation date.`);
      return;
    }
    if (!Number.isFinite(marketValue) || marketValue === 0) {
      // keep real NEGATIVE values (short options/liabilities); drop only missing/zero (no data)
      errors.push(`Row ${index + 1}: missing market value, or shares and price.`);
      return;
    }
    const assignment = classifyHolding(ticker, assetName, read(row, mapping.sleeve));
    holdings.push({
      id: `${ticker || assetName}-${index}`,
      snapshotId: `snapshot-${valuationDate}`,
      valuationDate,
      brokerageAccount: read(row, mapping.account),
      ticker,
      assetName,
      shares: Number.isFinite(shares) ? shares : 0,
      price: Number.isFinite(price) ? price : marketValue,
      marketValue,
      sleeve: assignment.sleeve,
      assignmentSource: assignment.source,
      importedSleeve: read(row, mapping.sleeve).trim(),   // what the CSV carried (for the override badge)
      costBasis: optionalNumber(read(row, mapping.costBasis)),
      beta: optionalNumber(read(row, mapping.beta)),
      sourceRow: row,
    });
  });
  return { holdings, errors };
}

function classifyHolding(ticker, assetName, importedSleeve) {
  const imported = importedSleeve.trim();
  const manual = state.assignments[assignmentKey(ticker, assetName)];
  // 2026-07 review: a deliberate MANUAL override wins over the imported (and auto) sleeve — honoring
  // the UI's promise that "manual sleeve edits are saved locally and reused on future imports". The
  // old order (imported > manual) silently discarded the correction on the next Load Full Book, whose
  // rows carry an authoritative Sleeve column. A manual sleeve sitting on top of a DIFFERENT imported
  // one is badged in the table (see the All-view sleeveCell) so a stale override is never invisible.
  if (manual) return { sleeve: manual, source: "manual" };
  if (imported) return { sleeve: imported, source: "imported" };
  const auto = autoClassify(ticker, assetName);
  return auto ? { sleeve: auto, source: "auto" } : { sleeve: "Unclassified", source: "unclassified" };
}

// Mirrors portfolio_analysis.py classify_code(): exact ticker → longest name keyword →
// CUSIP fallback → null. Returns the display sleeve NAME (via codeToName), or null when
// nothing matches (→ Unclassified). If the shared rules file failed to load there is NO
// fallback table (removed in the Class-C consolidation) — everything routes to Unclassified
// and the red rulesBanner warns the operator the classifier is degraded.
function autoClassify(ticker, assetName) {
  const t = normalizeTicker(ticker);
  const name = assetName.toLowerCase();
  const R = CLASSIFICATION_RULES;
  if (R) {
    let code = R.tickerRules[t];
    if (!code) {
      // P3-16 (2026-07-03): LONGEST matched keyword wins (specificity beats array
      // order — 'treasury' over 'bond', 'international' over 'core equity'); mirrors
      // portfolio_analysis.classify_code + the generator reference classify().
      let best = null, bestLen = -1;
      for (const rule of R.nameRules) {
        for (const k of rule.keywords) {
          if (k.length > bestLen && name.includes(k)) { best = rule.code; bestLen = k.length; }
        }
      }
      code = best;
    }
    if (!code) {
      for (const fb of R.fallbackRules) {
        if (new RegExp(fb.symbolPattern).test(t) && (!fb.nameAny || fb.nameAny.some((k) => name.includes(k)))) {
          code = fb.code;
          break;
        }
      }
    }
    return code ? (R.codeToName[code] || code) : null;
  }
  return null;   // rules failed to load → Unclassified (rulesBanner is showing)
}

